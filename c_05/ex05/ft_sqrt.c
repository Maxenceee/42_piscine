#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	sqrt;

	sqrt = 46341;
	i = 1;
	while (i <= sqrt)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
