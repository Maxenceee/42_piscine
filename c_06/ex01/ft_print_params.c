#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;

	i = 1;
	if (argc)
	{
		while (argv[i])
		{
			while (*argv[i])
			{
				write(1, argv[i], 1);
				argv[i]++;
			}
			write(1, "\n", 1);
			i++;
		}
	}
	return (0);
}
